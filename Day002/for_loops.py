# For loop: prints numbers from 0 to 4
for i in range(5):
    print(i)

# Prints even numbers from 0 to 10
for i in range(0, 11, 2):
    print(i)

# Prints odd numbers from 1 to 11
for i in range(1, 12, 2):
    print(i)

# Reverse numbers (0 to -4)
for i in range(0, -5, -1):
    print(i)

# Printing without using a variable, using "_"
for _ in range(3):
    print("Hello")
